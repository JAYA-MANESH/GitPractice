package online_programs;

public class Swapping_2_with {
	
	public static void main(String[] args) {
		int a=10; //20
		int b=20;
		int c; //10
		System.out.println("before swap");
		System.out.println("a "+a);
		System.out.println("b "+b);
		
		c=a;
		a=b;
		b=c;
		
		
		System.out.println("after swap");
		System.out.println("a "+a);
		System.out.println("b "+b);
	}

}
